package pl.excercise.java_zaawansowana_programowanie.zad17;

public class Converter {

    public double metersToYards(int value) {
        return 1.0936d * value;
    }

    public double yardsToMeters(int value) {
        return 0.9144d * value;
    }

    public double centimetersToInches(int value) {
        return 0.3937d * value;
    }

    public double inchesToCentimeters(int value) {
        return 2.54d * value;
    }

    public double kilometersToMiles(int value) {
        return 0.6213 * value;
    }

    public double milesToKilometers(int value) {
        return 1.6093 * value;
    }


}
